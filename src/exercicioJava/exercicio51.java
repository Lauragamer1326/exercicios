package exercicioJava;

public class exercicio51 {
    public static void main(String[] args) {
        // Inicialização da variável de controle
        int i = 1;

        // Loop do-while para exibir os números de 1 a 10
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
    }
}
