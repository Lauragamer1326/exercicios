package exercicioJava;

public class exercicio77 {
    public static void main(String[] args) {
        // Utilizando um loop for para percorrer os números de 50 a 1
        for (int i = 50; i >= 1; i--) {
            // Verifica se o número é ímpar
            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    }
}
